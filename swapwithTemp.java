class swapwithTemp {
    public static void main(String orgs[]) {
        int a = 14;
        int b = 10;
        int temp;
        System.out.println("Swaped numbers:");
        temp = a;
        a = b;
        b = temp;
        
        System.out.println("a = " + a);
        System.out.println("b = " + b);
       
    }
}



